c ************************************************************************
c ************************************************************************
c ************************************************************************
c ************************************************************************
c ************************************************************************
c ************************************************************************
      SUBROUTINE MINEIG(W)
      implicit none
c ************************************************************************
      DOUBLE PRECISION W
c     .. arrays in common
      INTEGER     IUP,IDO,NUM
      DOUBLE PRECISION WUP,WDO
      COMMON /EIGENV/ WUP,WDO,NUM,IUP,IDO

      INTRINSIC DABS
c ------------------------------------------------------------------------
      IF (IDO+IUP .EQ.0) STOP 'MINEIG'
      IF (IDO.GT.0) THEN
        W = WDO
        IF (IUP.GT.0 .AND. DABS(WUP).LT.DABS(WDO)) W = WUP
      ELSE
        W = WUP
      END IF
      RETURN
      END
